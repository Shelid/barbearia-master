export const SPAIN_REGION_NAMES = SPAIN_REGIONS.map((region) => region.name);

const normalizeLocationText = (value: string) =>
  value
    .toLowerCase()
    .normalize('NFD')
    .replace(/[\u0300-\u036f]/g, '')
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '');

export function getSpainRegionByName(regionName: string) {
  const normalized = normalizeLocationText(regionName);
  return SPAIN_REGIONS.find((region) => region.slug === normalized || normalizeLocationText(region.name) === normalized);
}

export function getSpainMunicipalitiesForRegion(regionName: string): readonly string[] {
  return getSpainRegionByName(regionName)?.municipalities ?? [];
}

export function isSpainMunicipalityInRegion(regionName: string, municipalityName: string) {
  const normalizedMunicipality = normalizeLocationText(municipalityName);
  return getSpainMunicipalitiesForRegion(regionName).some(
    (municipality) => normalizeLocationText(municipality) === normalizedMunicipality
  );
}
